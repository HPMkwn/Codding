lint solve()
{
    int N;
    cin >> N;
    vector<int> A(N);
    cin >> A;
    vector<int> cnt(32);
    lint ret = 0;
    for (auto a : A)
    {
        int d = 30;
        while ((1 << d) > a) d--;
        ret += cnt[d];
        cnt[d]++;
    }
    return ret;
}
 
int main()
{
    int T;
    cin >> T;
    while (T--) cout << solve() << '\n';

}
